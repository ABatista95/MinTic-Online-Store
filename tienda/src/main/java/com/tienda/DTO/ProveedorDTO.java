package com.tienda.DTO;

public class ProveedorDTO {

	private Integer nit_proveedor;
    private String direccion_proveedor;
    private String ciudad_proveedor;
    private String nombre_proveedor;
    private String telefono_proveedor;

public Integer getNitProveedor() {
		return nit_proveedor;
	}
public void setNitProveedor(Integer nit_proveedor) {
		this.nit_proveedor = nit_proveedor;
	}

public String getDireccionProveedor() {
	return direccion_proveedor;
}
public void setDireccionProveedor(String direccion_proveedor) {
	this.direccion_proveedor = direccion_proveedor;
}

public String getCiudadProveedor() {
	return ciudad_proveedor;
}
public void setCiudadProveedor(String ciudad_proveedor) {
	this.ciudad_proveedor = ciudad_proveedor;
}

public String getNombreProveedor() {
	return nombre_proveedor;
}
public void setNombreProveedor(String nombre_proveedor) {
	this.nombre_proveedor = nombre_proveedor;
}

public String getTelefonoProveedor() {
	return telefono_proveedor;
}
public void setTelefonoProveedor(String telefono_proveedor) {
	this.telefono_proveedor = telefono_proveedor;
}
  


}

